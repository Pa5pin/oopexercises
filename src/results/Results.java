package results;

public class Results {
	
		
		public int Physics;
		public int Chemistry;
		public int Biology;
		public int Total;
		public int Percentage;
		
		public Results(int physics, int chemistry, int biology, int Total, int Percentage) {
			super();
			this.Physics = physics;
			this.Chemistry = chemistry;
			this.Biology = biology;
			this.Total = Total;
			this.Percentage = Percentage;
		}

}
